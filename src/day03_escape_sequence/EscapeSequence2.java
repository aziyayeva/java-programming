package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args) {

        System.out.println(" \"iphone case\", \"iphone\", \n \"xbox\", \"webcam\", \"monitor\",");
        System.out.println("\"iphone\" ");
        System.out.println("\"xbox\" ");
        System.out.println("\"webcam\" ");
        System.out.println("\"monitor\" ");
        System.out.println("\"computer\" ");
        System.out.println();

        System.out.println("\'iphone\' ");
        System.out.println();

        System.out.println("\\iphone\\ ");
        System.out.println();

        System.out.println("\n iphone\n ");
        System.out.println();

        System.out.println("\tiphone\t ");
        System.out.println();


        System.out.println("   ^   ");
        System.out.println("  / \\ ");
        System.out.println(" |   |");
        System.out.println(" -----  ");

    }
}
